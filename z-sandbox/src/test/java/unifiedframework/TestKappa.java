    private static final String[] names = {
        "RSA-100", "RSA-129", "RSA-155", "RSA-160", "RSA-170", "RSA-180", "RSA-190", "RSA-200", "RSA-210", "RSA-220", "RSA-230", "RSA-240", "RSA-250", "RSA-260", "RSA-270", "RSA-280", "RSA-290", "RSA-300"
    };

    @Test
    @DisplayName("Test Kappa computation on RSA challenges")
    public void testKappaOnRSAChallenges() {
        System.out.println("Testing Kappa computation on RSA challenges...");
        BigInteger autoThresh = BigInteger.valueOf(10).pow(20);
        System.out.println("AUTO_APPROX_THRESHOLD = " + autoThresh);
        System.out.println("Semiprime approximation (d=4) will be used for n > threshold");
        System.out.println();

        double[] results = Kappa.batchKappa(rsaExamples, null, false, MC); // Use auto approx, no high prec

        System.out.println("Computing κ(n) values using semiprime approximation (d=4) by default...");
        for (int i = 0; i < names.length; i++) {
            System.out.printf("%-12s κ(n) = %.6f%n", names[i], results[i]);
        }
        System.out.println();

        System.out.println("Statistical Analysis:");
        double mean = Arrays.stream(results).average().orElse(0);
        double variance = Arrays.stream(results).map(x -> Math.pow(x - mean, 2)).average().orElse(0);
        double std = Math.sqrt(variance);
        double min = Arrays.stream(results).min().orElse(0);
        double max = Arrays.stream(results).max().orElse(0);
        System.out.printf("Mean κ: %.6f%n", mean);
        System.out.printf("Std Dev: %.6f%n", std);
        System.out.printf("Min κ: %.6f%n", min);
        System.out.printf("Max κ: %.6f%n", max);
        System.out.println();

        double[] ci = Kappa.bootstrapCi(results, 10000, 123, 0.05);
        System.out.printf("Bootstrap 95%% CI on mean κ: [%.6f, %.6f]%n", ci[0], ci[1]);
        System.out.println();

        System.out.println("Hypothesis Validation:");
        double eSq = Math.exp(2.0);
        System.out.println("Expected for semiprimes: κ(n) ≈ 4 * ln(n) / e^2");
        for (int i = 0; i < names.length; i++) {
            double expected = 4 * Math.log(rsaExamples[i].doubleValue()) / eSq;
            double ratio = results[i] / expected;
            System.out.printf("  %s: κ(n)/expected = %.6f%n", names[i], ratio);
        }
        System.out.println();

        double[] fit = Kappa.fitLinearLnVsKappa(rsaExamples, results);
        System.out.println("Linear fit κ ~ a*ln(n) + b:");
        System.out.printf("  a (slope) = %.6e%n", fit[0]);
        System.out.printf("  b (intercept) = %.6e%n", fit[1]);
        System.out.printf("  R² = %.6f%n", fit[2]);
        System.out.println();

        // Assertions
        assertTrue(Arrays.stream(results).allMatch(Double::isFinite));
        assertTrue(mean > 200 && mean < 300); // Approx 4/e^2
        assertTrue(fit[2] > 0.99); // High R2
    }
